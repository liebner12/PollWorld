export const validatePasswordLength = (password = "") => {
  return password.length >= 8 ? "" : "Hasło powinno zawierać minimum 8 znaków";
};

export const validateTwoPasswords = (password, secondPassword) => {
  return password == secondPassword ? "" : "Hasła nie mogą się różnić";
};

export const cantBeEmpty = (text) => {
  return text ? "" : "Pole nie może być puste";
};

export const onlyNumbers = (text) => {
  return /^\d+$/.test(text) ? "" : "Pole może zawierać tylko cyfry";
};
